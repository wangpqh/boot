package com.mypqh.common.train;

public class Seat {
    String type;
    String saleFlag;
    String price;
    String restTicketNum;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSaleFlag() {
        return saleFlag;
    }

    public void setSaleFlag(String saleFlag) {
        this.saleFlag = saleFlag;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getRestTicketNum() {
        return restTicketNum;
    }

    public void setRestTicketNum(String restTicketNum) {
        this.restTicketNum = restTicketNum;
    }
}
